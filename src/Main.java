package MenggunakanString;
public class Main {
    public static void main(String[] args) {
        String txt = "Nama : Kurniasih";
        String txt1 = "Jurusan : Teknik Informatika";
        String txt2 = "Mata Kuliah : Pemrograman 2";
        String txt3 = "Kode Kelas : FS204E";

        System.out.println(txt.toUpperCase());
        System.out.println(txt.toLowerCase());

        System.out.println(txt1.toUpperCase());
        System.out.println(txt1.toLowerCase());

        System.out.println(txt2.toUpperCase());
        System.out.println(txt2.toLowerCase());

        System.out.println(txt3.toUpperCase());
        System.out.println(txt3.toLowerCase());


    }
}